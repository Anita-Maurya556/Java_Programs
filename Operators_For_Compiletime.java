package java_programms;

public class Operators_For_Compiletime {
	static void addition()
	{
		int a=100;
		int b=200;
		int sum=a+b;
		System.out.println("the addition of two number is-> "+sum);
	}
	 static void Subtraction()
	{
		int a=50;
		int b=80;
		int sub=a-b;
		System.out.println("The subtraction of two number is->"+sub);
	}
	 static void Multiplecation()
		{
			int a=10;
			int b=10;
			int mul=a*b;
			System.out.println("The multiplecation of two number is->"+mul);
		}
	 static void Division()
		{
			int a=150;
			int b=50;
			int div=a/b;
			System.out.println("The division of two number is->"+div);
		}
	   static void Modulas()
		{
			int a=10;
			int b=2;
			int mod=a%b;
			System.out.println("The Modulas of two number is->"+mod);
		}
	

	public static void main(String[] args) {
		addition();
		Subtraction();
		Multiplecation();
		Division();
		Modulas();
		
		
		
		
		
		

	}

}
